    //Proses Penyimpanan Kunci
    private void simpanKunci(String jenisKunci, String Kunci) throws IOException {
        this.filesimpanKunci = new File("FolderOperasi/LokasiKunci/" + jenisKunci);
        try {
            this.bwsimpanKunci = new BufferedWriter(new FileWriter(filesimpanKunci));
            this.bwsimpanKunci.write(Kunci);
            this.bwsimpanKunci.close();
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(FormUtama.this, "Pastikan Lokasi Penyimpanan Ada!", "Kesalahan", JOptionPane.WARNING_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(FormUtama.this, "Pastikan Lokasi Penyimpanan Ada!", "Kesalahan", JOptionPane.WARNING_MESSAGE);
        }
    }

