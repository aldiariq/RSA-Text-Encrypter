            this.enkripsi = new Enkripsi(fileAsli);
            this.setVariabelrsa(enkripsi.getP().toString(), enkripsi.getQ().toString(), enkripsi.getN().toString(), enkripsi.getTn().toString(), enkripsi.getE().toString(), enkripsi.getD().toString(), enkripsi.getLokasiFileDekripsi());
