    private void btnDekripsiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDekripsiActionPerformed
        // TODO add your handling code here:
        if (this.Kuncid == null || this.Kuncin == null || this.fileEnkripsi == null) {
            JOptionPane.showMessageDialog(FormUtama.this, "Pastikan File Enkripsi dan Kunci Sudah Dipilih", "Kesalahan", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                BigInteger d = new BigInteger(this.Kuncid);
                BigInteger n = new BigInteger(this.Kuncin);

                this.dekripsi = new Dekripsi(d, n, this.fileEnkripsi);

                Thread thread = new Thread() {
                    @Override
                    public void run() {
                        try {
                            for (int i = 1; i <= 100; i++) {
                                pbDekripsi.setValue(i);
                                sleep(50);
                            }
                            JOptionPane.showMessageDialog(FormUtama.this, "Proses Enkripsi Selesai!", "Keterangan", JOptionPane.DEFAULT_OPTION);
                        } catch (InterruptedException ex) {
                        }
                    }
                };
                thread.start();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(FormUtama.this, "Pastikan File dan Kunci Enkripsi Sesuai!", "Kesalahan", JOptionPane.DEFAULT_OPTION);
            }
        }
    }//GEN-LAST:event_btnDekripsiActionPerformed

